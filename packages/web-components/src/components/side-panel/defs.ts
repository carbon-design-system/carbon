/**
 * Modal size.
 */
export enum SIDE_PANEL_SIZE {
  /**
   * Extra small size.
   */
  EXTRA_SMALL = 'xs',

  /**
   * Small size.
   */
  SMALL = 'sm',

  /**
   * Medium size.
   */
  MEDIUM = 'md',

  /**
   * Large size.
   */
  LARGE = 'lg',

  /**
   * 2X-Large size.
   */
  EXTRA_EXTRA_LARGE = '2xl',
}

export enum SIDE_PANEL_PLACEMENT {
  /** right / default */
  RIGHT = 'right',

  /** left  */
  LEFT = 'left',
}
