export const getLinks = (index) => {
  if (index > 0) {
    const links: TemplateResult[] = [];
    for (let i = 0; i < index; i++) {
      const link = html`<cds-link href="#"> Link action </cds-link>`;
      links.push(link);
    }
    return links;
  } else {
    return null;
  }
};
