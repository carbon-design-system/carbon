'use strict';

const fs = require('fs');
const path = require('path');

const DTCG_PATH = path.resolve(__dirname, '../../src/dtcg/motion.json');

const JS_BANNER = `// Code generated by @carbon/motion. DO NOT EDIT.
//
// Copyright IBM Corp. 2018, 2026
//
// This source code is licensed under the Apache-2.0 license found in the
// LICENSE file in the root directory of this source tree.
`;

const SCSS_BANNER = `// Code generated by @carbon/motion. DO NOT EDIT.
//
// Copyright IBM Corp. 2018, 2026
//
// This source code is licensed under the Apache-2.0 license found in the
// LICENSE file in the root directory of this source tree.
//
`;

/**
 * Flatten the nested DTCG token tree into a Map of token-name → { resolved, description }.
 *
 * Duration tokens:  duration.fast.01 → "duration-fast-01": "70ms"
 * Easing tokens:    easing.standard.productive → "easing-standard-productive": "cubic-bezier(...)"
 *
 * @param {object} dtcgTokens - Parsed DTCG JSON
 * @returns {Map<string, { resolved: string, description: string }>}
 */
function flattenDTCGTokens(dtcgTokens) {
  const tokens = new Map();

  function traverse(obj, pathSegments) {
    for (const [key, value] of Object.entries(obj)) {
      if (key.startsWith('$')) continue;

      const segments = [...pathSegments, key];

      if (value && typeof value === 'object' && value.$value !== undefined) {
        const name = segments.join('-');
        const raw = value.$value;

        let resolved;
        if (Array.isArray(raw)) {
          // cubicBezier: [x1, y1, x2, y2] → "cubic-bezier(x1, y1, x2, y2)"
          resolved = `cubic-bezier(${raw.join(', ')})`;
        } else if (
          raw &&
          typeof raw === 'object' &&
          'value' in raw &&
          'unit' in raw
        ) {
          // duration: { value: 70, unit: "ms" } → "70ms"
          resolved = `${raw.value}${raw.unit}`;
        } else {
          resolved = String(raw);
        }

        tokens.set(name, { resolved, description: value.$description || '' });
      } else if (value && typeof value === 'object') {
        traverse(value, segments);
      }
    }
  }

  traverse(dtcgTokens, []);
  return tokens;
}

/**
 * Convert a dash-joined token name to camelCase.
 * e.g. "duration-fast-01" → "durationFast01"
 */
function toCamelCase(name) {
  return name.replace(/-([a-z0-9])/gi, (_, ch) => ch.toUpperCase());
}

/**
 * Build a plain JS module + sibling .d.ts from DTCG motion tokens.
 * Written to js/generated/tokens.{js,d.ts} — gitignored.
 * Consumed by the hand-authored src/index.ts.
 *
 * Emits: V11 duration exports, V10 deprecated aliases, easings map,
 * unstable_tokens array.
 * Does NOT emit the motion() helper or TS types — those stay in src/index.ts.
 *
 * @returns {{ js: string, dts: string }}
 */
function buildDTCGMotionJS() {
  const dtcgTokens = JSON.parse(fs.readFileSync(DTCG_PATH, 'utf8'));
  const tokens = flattenDTCGTokens(dtcgTokens);

  const jsLines = [JS_BANNER];
  const dtsLines = [JS_BANNER];

  // ── Duration tokens (V11 canonical) ──────────────────────────────────────
  jsLines.push('// Duration tokens (V11)');
  dtsLines.push('// Duration tokens (V11)');
  const durationTokens = [];
  for (const [name, { resolved, description }] of tokens) {
    if (!name.startsWith('duration-')) continue;
    const camel = toCamelCase(name);
    durationTokens.push(camel);
    if (description) {
      jsLines.push(`/** ${description} */`);
      dtsLines.push(`/** ${description} */`);
    }
    jsLines.push(`export const ${camel} = '${resolved}';`);
    dtsLines.push(`export declare const ${camel}: string;`);
  }

  // ── Duration tokens (V10 deprecated aliases) ──────────────────────────────
  jsLines.push('');
  jsLines.push('// Duration tokens (V10 — deprecated aliases)');
  dtsLines.push('');
  dtsLines.push('// Duration tokens (V10 — deprecated aliases)');
  const v10Aliases = [];
  for (const camel of durationTokens) {
    const v10Name =
      camel.charAt('duration'.length).toLowerCase() +
      camel.slice('duration'.length + 1);
    v10Aliases.push(v10Name);
    jsLines.push(`/** @deprecated Use \`${camel}\` instead */`);
    jsLines.push(`export const ${v10Name} = ${camel};`);
    dtsLines.push(`/** @deprecated Use \`${camel}\` instead */`);
    dtsLines.push(`export declare const ${v10Name}: string;`);
  }

  // ── Easing map ────────────────────────────────────────────────────────────
  jsLines.push('');
  jsLines.push('// Easing tokens');
  jsLines.push('export const easings = {');
  dtsLines.push('');
  dtsLines.push('// Easing tokens');
  dtsLines.push(
    "export declare const easings: Record<'standard' | 'entrance' | 'exit', Record<'productive' | 'expressive', string>>;"
  );

  for (const easingName of ['standard', 'entrance', 'exit']) {
    jsLines.push(`  ${easingName}: {`);
    for (const mode of ['productive', 'expressive']) {
      const key = `easing-${easingName}-${mode}`;
      const entry = tokens.get(key);
      if (!entry) throw new Error(`Missing DTCG token: ${key}`);
      jsLines.push(`    ${mode}: '${entry.resolved}',`);
    }
    jsLines.push(`  },`);
  }
  jsLines.push('};');

  // ── unstable_tokens ───────────────────────────────────────────────────────
  jsLines.push('');
  jsLines.push('export const unstable_tokens = [');
  dtsLines.push('');
  dtsLines.push('export declare const unstable_tokens: readonly string[];');
  for (const camel of durationTokens) jsLines.push(`  '${camel}',`);
  for (const v10 of v10Aliases) jsLines.push(`  '${v10}',`);
  jsLines.push('];');
  jsLines.push('');

  return { js: jsLines.join('\n'), dts: dtsLines.join('\n') };
}

/**
 * Build a generated Sass partial from DTCG motion tokens.
 * Written to scss/generated/_tokens.scss — gitignored.
 * Consumed by the hand-authored index.scss.
 *
 * Emits: $easings map, $duration-* V11 variables, $fast-* V10 deprecated aliases.
 *
 * @returns {string}
 */
function buildDTCGMotionSCSS() {
  const dtcgTokens = JSON.parse(fs.readFileSync(DTCG_PATH, 'utf8'));
  const tokens = flattenDTCGTokens(dtcgTokens);

  const lines = [SCSS_BANNER];

  // ── $easings map ──────────────────────────────────────────────────────────
  lines.push('/// Common component easings');
  lines.push('/// @type Map');
  lines.push('/// @access public');
  lines.push('/// @group @carbon/motion');
  lines.push('$easings: (');
  for (const easingName of ['standard', 'entrance', 'exit']) {
    lines.push(`  ${easingName}: (`);
    for (const mode of ['productive', 'expressive']) {
      const key = `easing-${easingName}-${mode}`;
      const entry = tokens.get(key);
      if (!entry) throw new Error(`Missing DTCG token: ${key}`);
      lines.push(`    ${mode}: ${entry.resolved},`);
    }
    lines.push(`  ),`);
  }
  lines.push(') !default;');
  lines.push('');

  // ── V11 duration variables ────────────────────────────────────────────────
  for (const [name, { resolved, description }] of tokens) {
    if (!name.startsWith('duration-')) continue;
    if (description) lines.push(`/// ${description}`);
    lines.push('/// @access public');
    lines.push('/// @type Duration');
    lines.push('/// @group @carbon/motion');
    lines.push(`$${name}: ${resolved} !default;`);
    lines.push('');
  }

  // ── V10 deprecated aliases ────────────────────────────────────────────────
  lines.push('/// V10 backwards compatibility tokens');
  const v10Map = {
    'duration-fast-01': 'fast-01',
    'duration-fast-02': 'fast-02',
    'duration-moderate-01': 'moderate-01',
    'duration-moderate-02': 'moderate-02',
    'duration-slow-01': 'slow-01',
    'duration-slow-02': 'slow-02',
  };
  for (const [v11, v10] of Object.entries(v10Map)) {
    lines.push('/// @access public');
    lines.push('/// @deprecated');
    lines.push('/// @type Duration');
    lines.push('/// @group @carbon/motion');
    lines.push(`$${v10}: $${v11} !default;`);
    lines.push('');
  }

  return lines.join('\n');
}

module.exports = { buildDTCGMotionJS, buildDTCGMotionSCSS };
