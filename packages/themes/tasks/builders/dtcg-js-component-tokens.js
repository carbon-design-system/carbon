'use strict';

const fs = require('fs-extra');
const path = require('path');
const {
  convertDTCGComponentTokens,
  normalizeComponentThemeName,
} = require('./dtcg-converter');

const FILE_BANNER = `// Code generated by @carbon/themes. DO NOT EDIT.
//
// Copyright IBM Corp. 2018, 2025
//
// This source code is licensed under the Apache-2.0 license found in the
// LICENSE file in the root directory of this source tree.
`;

/**
 * Convert a kebab-case string to camelCase.
 * e.g. "button-danger-active" → "buttonDangerActive"
 */
function kebabToCamel(str) {
  return str.replace(/-([a-z0-9])/g, (_, ch) => ch.toUpperCase());
}

/**
 * Serialise a JS value to a source-code string.
 */
function toSource(value) {
  if (typeof value === 'string') {
    return `'${value.replace(/\\/g, '\\\\').replace(/'/g, "\\'")}'`;
  }
  return JSON.stringify(value);
}

/**
 * Generate a JS module for one component's tokens from its DTCG JSON file and
 * write it to `js/generated/component-tokens/<componentName>.js`, plus a
 * sibling `.d.ts` so that TypeScript can resolve types without needing to
 * compile the JS file.
 *
 * The exported shape matches the existing hand-authored tokens exactly:
 *   export const buttonDangerActive = { whiteTheme: '...', g10: '...', ... };
 *
 * @param {string} componentName - e.g. 'button', 'tag', 'content-switcher'
 * @param {string} outDir        - Absolute path to the output directory
 */
function buildDTCGJsComponentTokensFile(componentName, outDir) {
  const dtcgPath = path.resolve(
    __dirname,
    `../../src/dtcg/components/${componentName}.json`
  );

  if (!fs.existsSync(dtcgPath)) {
    console.warn(
      `Warning: ${componentName}.json not found, skipping JS component tokens`
    );
    return;
  }

  const dtcgTokens = JSON.parse(fs.readFileSync(dtcgPath, 'utf8'));
  // Returns { 'button-danger-active': { white: '#...', g10: '#...', ... }, ... }
  const componentTokens = convertDTCGComponentTokens(dtcgTokens);

  const jsLines = [FILE_BANNER];
  const dtsLines = [FILE_BANNER];

  for (const [kebabName, themeValues] of Object.entries(componentTokens).sort(
    ([a], [b]) => a.localeCompare(b)
  )) {
    const camelName = kebabToCamel(kebabName);

    // Normalise theme keys: 'white' → 'whiteTheme', 'g10' → 'g10', etc.
    const normalisedValues = {};
    for (const [theme, value] of Object.entries(themeValues)) {
      // normalizeComponentThemeName converts 'white'→'white-theme', 'g10'→'g-10'
      // but the JS API uses 'whiteTheme', 'g10' (no dash). Re-apply camelCase.
      const normTheme = kebabToCamel(normalizeComponentThemeName(theme));
      normalisedValues[normTheme] = value;
    }

    const props = Object.entries(normalisedValues)
      .map(([k, v]) => `  ${k}: ${toSource(v)}`)
      .join(',\n');
    const dtsProps = Object.keys(normalisedValues)
      .map((k) => `  ${k}: string`)
      .join(';\n');

    jsLines.push(`export const ${camelName} = {\n${props},\n};`);
    dtsLines.push(`export declare const ${camelName}: {\n${dtsProps};\n};`);
  }

  fs.ensureDirSync(outDir);
  fs.writeFileSync(
    path.join(outDir, `${componentName}.js`),
    jsLines.join('\n') + '\n'
  );
  fs.writeFileSync(
    path.join(outDir, `${componentName}.d.ts`),
    dtsLines.join('\n') + '\n'
  );
}

module.exports = buildDTCGJsComponentTokensFile;
