/**
 * Breakpoints for accordion items. It's different from one in `@carbon/layout` library.
 */
export enum ACCORDION_ITEM_BREAKPOINT {
  /**
   * Small breakpoint.
   */
  SMALL = 'sm',

  /**
   * Medium breakpoint.
   */
  MEDIUM = 'md',
}

/**
 * Button size.
 */
export enum ACCORDION_SIZE {
  /**
   * Regular size.
   */
  REGULAR = '',

  /**
   * Small size.
   */
  SMALL = 'sm',

  /**
   * X-Large size.
   */
  EXTRA_LARGE = 'xl',
}
